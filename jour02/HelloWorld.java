package jour02;

// Cette classe définit le point d'entrée du programme Java
public class HelloWorld {
    /*
     * La méthode main est le point d'entrée du programme.
     * Quand le programme est exécuté, cette méthode est appelée
     * et le code à l'intérieur de cette méthode est exécuté.
     */
    public static void main(String[] args) {
        // Affiche "Hello world" dans le terminal
        System.out.println("Hello world");
    }
}