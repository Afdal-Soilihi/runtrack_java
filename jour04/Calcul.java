package jour04;

public class Calcul {
    public static void main(String[] args) {
        // Déclarer la variable nombre et lui assigner la valeur 7
        int nombre = 7;
        
        // Afficher la valeur initiale de nombre
        System.out.println("Valeur initiale de nombre : " + nombre);
        
        // Ajouter 3 à la variable nombre
        nombre += 3;
        
        // Afficher la valeur de nombre après l'ajout
        System.out.println("Valeur de nombre après ajout de 3 : " + nombre);
        
        // Multiplier la valeur de nombre par 5
        nombre *= 5;
        
        // Afficher la valeur de nombre après la multiplication
        System.out.println("Valeur de nombre après multiplication par 5 : " + nombre);
    }
}
