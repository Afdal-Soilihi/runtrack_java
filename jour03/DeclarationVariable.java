package jour03;

public class DeclarationVariable {
    public static void main(String[] args) {
        // Déclarer trois variables de type int sur une seule ligne et les initialiser à 0
        int a = 0, b = 0, c = 0;

        // Afficher les valeurs des variables pour vérifier qu'elles sont bien initialisées
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
    }
}
